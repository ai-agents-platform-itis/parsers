def build_campaign_settings(preset_name: str) -> dict[str, Any]:
    """
    Собрать settings для новой кампании из пресета (глубокая копия).

    Копия — принципиально: кампания дальше живёт своей жизнью (правки из
    админки), пресет остаётся неизменным шаблоном.
    """
    import copy

    if preset_name not in NICHE_PRESETS:
        raise ValueError(
            f"Неизвестный пресет '{preset_name}'. "
            f"Доступные: {', '.join(sorted(NICHE_PRESETS))}"
        )
    return copy.deepcopy(NICHE_PRESETS[preset_name])
